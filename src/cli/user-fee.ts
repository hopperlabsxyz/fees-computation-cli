import { processVault } from "core/processVault";
import { mergeDeals, parseDeals, type AllDeals } from "parsing/parseDeals";
import { parseArguments } from "utils/parseArguments";
import type { Address } from "viem";
import type { Command } from "@commander-js/extra-typings";
import type { Point } from "core/types";
import { parsePoints } from "parsing/parsePoints";

export function setUserFeeCommand(command: Command) {
  command
    .command("user-fee")
    .description(
      "Calculate and generate fee reports for a specified vault, including referral rewards and rebates for all users"
    )
    .argument("chainId:VaultAddress")
    .requiredOption(
      "-f, --from-block <number>",
      "Starting block number for fee computation (exclusive). Use 'find-blocks' command to find the appropriate block number"
    )
    .requiredOption(
      "-t, --to-block <number>",
      "Ending block number for fee computation (inclusive). Use 'find-blocks' command to find the appropriate block number"
    )
    .option("-r, --readable", "Format the output in a human-readable format")
    .option(
      "-o, --output",
      "Will save the result in output/user-fee in a file with following format: <chainId>-<vaultAddress>-<from-block>-<to-block>.csv"
    )
    .option(
      "--silent",
      "This will prevent the printing of the output on stdout",
      false
    )
    .option(
      "-d, --deals <string>",
      "Path to the configuration file containing OTC (Over-The-Counter) deals. The amount of % is express in 10^2. For example, 8% is express 800 into the csv file."
    )
    .option(
      "--fee-rebate-rate <number>",
      "Bips of fees to be refunded to the referred user (e.g., 500 means 5% of fees are refunded, 5 means 0.05% of fees are refunded)",
      "500"
    )
    .option(
      "--fee-reward-rate <number>",
      "Bips of fees to be distributed to the referrer (e.g., 1500 means referrer receives 15% of fees generated by the referred user)",
      "1500"
    )
    .option(
      "--points <string>",
      "A path to a file containing the evolutions of points through time of the form timestamp,amount. For each of those points in time \
       the program will distribute all the new points proportionnaly to the shares holders."
    )
    .addHelpText(
      "after",
      `
Example:
  $ bun user-fee 1:0x07ed467acd4ffd13023046968b0859781cb90d9b -f 1000000 -l 2000000 -r -o -d dealsExample.csv
    `
    )
    .action(async (args, options) => {
      const vault = parseArguments(args);

      let deals: AllDeals = {};
      let points: Point[] = [];
      if (options.deals) deals = await parseDeals(options.deals);
      if (options.points) points = await parsePoints(options.points);

      let vaultDeals: Record<Address, number> = {};
      if (deals[vault.chainId])
        vaultDeals =
          deals[vault.chainId][vault.address.toLowerCase() as Address] || {};
      vaultDeals = mergeDeals(deals[0]?.["0x0"] || {}, vaultDeals);

      const result = await processVault({
        fromBlock: Number(options!.fromBlock!),
        toBlock: Number(options!.toBlock!),
        deals: vaultDeals,
        readable: options!.readable!,
        feeRebateRate: Number(options!.feeRebateRate!),
        feeRewardRate: Number(options!.feeRewardRate!),
        vault,
        points,
      });

      const csv = convertToCSV(result);
      if (options.silent == false) {
        console.log(csv);
      }
      if (options.output) {
        try {
          const file = Bun.file(
            `./output/user-fee/${vault.chainId}-${vault.address}-${options.fromBlock}-${options.toBlock}.csv`
          );
          await file.write(csv);
          console.log(`CSV report written to: ${file.name}`);
        } catch (error: any) {
          console.error("Error writing CSV file:", error.message);
          console.log("CSV content:");
          console.log(csv);
        }
      }
    });
}

function convertToCSV(vault: {
  chainId: number;
  address: string;
  pricePerShare: number;
  pointNames: string[];
  data: Record<
    string,
    {
      balance: number;
      fees: number;
      cashback: number;
      points: Record<string, bigint>;
    }
  >;
}) {
  const pointNamesString = vault.pointNames.reduce(
    (prev, cur) => `${prev},${cur}`,
    ""
  );
  const csvRows = [
    `chainId,vault,wallet,balance,fees,pricePerShare,cashback${pointNamesString}`, // CSV header
    ...Object.entries(vault.data).map(
      ([address, { balance, fees, cashback, points }]) => {
        if (
          address.toLowerCase() ==
          "0x1558b78560b88c75e9c95bbb9fda732f1e79f465".toLowerCase()
        )
          console.log(points);
        let str = `${vault.chainId},${vault.address},${address},${balance},${fees}`;
        str += `,${vault.pricePerShare},${cashback}`;
        str += pointsToCsv(points, vault.pointNames);
        return str;
      }
    ),
  ];
  return csvRows.join("\n");
}

function pointsToCsv(
  points: Record<string, bigint>,
  pointsName: string[]
): string {
  const a = pointsName.reduce((prev, cur) => {
    const value = points[cur] || 0n;
    return `${prev},${value}`;
  }, "");
  return a;
}
